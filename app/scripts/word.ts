function isAlphabet(str: string, pos: number) {
    let code = str.codePointAt(pos);
    if (!code) throw new Error('Code point is null');
    return ALPHABETS.has(code);
}

export function isWordBoundary(str: string, pos: number) {
    if (pos - 1 < 0 || str.length <= pos) {
        return true;
    } else {
        return !(isAlphabet(str, pos) && isAlphabet(str, pos - 1));
    }
}

